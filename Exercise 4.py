r = int(input("r = ")) #Receives r
import math #Imports math library
p2 = 2 * math.pi * r #Gets and names the the circumference of the circle
s = (r ** 2) * math.pi #Gets and names the area of the circle
print("2P = ",p2,",","S = ",s) #Prints the circumference and area of the circle due 'r' is considered as the radius of the circle